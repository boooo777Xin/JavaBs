public class If1 {

    public static void main(String[] args) {

        int j = 4;

        if(j > 5){
            System.out.println("数字は5以上です");
        } else {
            System.out.println("数字は5以下です");
        }

        if(j % 2 == 0){
            System.out.println("偶数");
        } else {
            System.out.println("奇数");
        }
        
    }
}
